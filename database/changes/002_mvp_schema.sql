create index idx_actividad_senior_responsable_id on actividad (senior_responsable_id);
create index idx_senior_grupo_senior_id on senior_grupo (senior_id);
create index idx_senior_grupo_grupo_id on senior_grupo (grupo_id);
create index idx_actividad_senior_actividad_id on actividad_senior (actividad_id);
create index idx_actividad_senior_senior_id on actividad_senior (senior_id);
create index idx_sesion_actividad_id on sesion (actividad_id);
create index idx_sesion_grupo_id on sesion (grupo_id);
create index idx_sesion_centro_id on sesion (centro_id);
create index idx_sesion_fecha on sesion (fecha);
